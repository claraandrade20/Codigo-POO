package Circulo;

public class Ponto1 {
	
    private double x; // Coeficiente angular
    private double y; // Coeficiente linear
    
    public Ponto1(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}

